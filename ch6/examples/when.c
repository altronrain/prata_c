/* when.c -- когда цикл завершается? */
#include <stdio.h>

int main(void)
{
	int n = 5;

	while (n++ < 7) {						/* в сравнении n = 5; затем n = 6 */
		printf ("n = %d\n", n);				/* n = 6 */
		n++;								/* n = 7 */
		printf ("Теперь n = %d\n", n);		/* n = 7 */
	}										/* выход после первой итерации */

	printf ("Цикл завершен.\n");

	return 0;
}

/*
$ ./when 
n = 6
Теперь n = 7
Цикл завершен.
*/