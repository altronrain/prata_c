/* q2.c -- Все переменные имеют тип int. Найдите значение каждой из них */
#include <stdio.h>

int main(void)
{
	int x;

	x = (int) 3.8 + 3.3;		/* x = 6 */
	printf("x = %d\n", x);
	x = (2 + 3) * 10.5;			/* x = 90 */
	printf("x = %d\n", x);
	x = 3 / 5 * 22.0;			/* x = 0 -- 3 / 5 уже целочисленное деление, которое дало 0 */
	printf("x = %d\n", x);
	x = 22.0 * 3 / 5;			/* x = 13 */
	printf("x = %d\n", x);

	return 0;
}

